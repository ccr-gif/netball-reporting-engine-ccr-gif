const POSITIONS = ['GS','GA','WA','C','WD','GD','GK'];

function scoreColor(score: number) {
  if (score === 0) return '#1e293b';
  if (score >= 70) return '#15803d';
  if (score >= 50) return '#ca8a04';
  return '#dc2626';
}

